-- Crear el usuario (el script corre como SYSTEM, tiene permisos para esto)
BEGIN
  EXECUTE IMMEDIATE 'CREATE USER USUARIO_AEROLINEA IDENTIFIED BY "LLego2005/"';
EXCEPTION
  WHEN OTHERS THEN
    IF SQLCODE != -1920 THEN  -- ORA-01920: usuario ya existe, ignorar
      RAISE;
    END IF;
END;
/

-- Asignar todos los permisos necesarios
GRANT CONNECT, RESOURCE TO USUARIO_AEROLINEA;
GRANT UNLIMITED TABLESPACE TO USUARIO_AEROLINEA;
GRANT CREATE SESSION TO USUARIO_AEROLINEA;
GRANT CREATE TABLE TO USUARIO_AEROLINEA;
GRANT CREATE SEQUENCE TO USUARIO_AEROLINEA;

ALTER USER USUARIO_AEROLINEA ACCOUNT UNLOCK;

-- ============================================================
-- SEQUENCES
-- ============================================================

CREATE SEQUENCE USUARIO_AEROLINEA.SEQ_PAISES
  MINVALUE 1
  MAXVALUE 9999999999999999999999999999
  INCREMENT BY 1
  START WITH 250
  NOCACHE NOORDER NOCYCLE;

CREATE SEQUENCE USUARIO_AEROLINEA.SEQ_USUARIOS
  MINVALUE 1
  MAXVALUE 9999999999999999999999999999
  INCREMENT BY 1
  START WITH 1
  NOCACHE NOORDER NOCYCLE;

-- ============================================================
-- TABLAS
-- Orden importante: primero tablas sin FK, luego las que
-- dependen de otras. Los constraints van inline para evitar
-- errores de orden de ejecución.
-- ============================================================

-- 1. PAISES (sin dependencias)
CREATE TABLE USUARIO_AEROLINEA.PAISES (
  ID       NUMBER GENERATED BY DEFAULT AS IDENTITY
             (MINVALUE 1 INCREMENT BY 1 START WITH 1 NOCACHE NOCYCLE)
             NOT NULL,
  NAME     VARCHAR2(100 BYTE) NOT NULL,
  ISO_NAME VARCHAR2(100 BYTE) NOT NULL,
  ALFA2    VARCHAR2(2 BYTE)   NOT NULL,
  ALFA3    VARCHAR2(3 BYTE)   NOT NULL,
  NUMERICO NUMBER(3,0),
  CONSTRAINT PK_PAISES       PRIMARY KEY (ID),
  CONSTRAINT UK_PAISES_ALFA2 UNIQUE (ALFA2),
  CONSTRAINT UK_PAISES_ALFA3 UNIQUE (ALFA3)
);

-- 2. USUARIOS (sin FK hacia otras tablas del schema)
CREATE TABLE USUARIO_AEROLINEA.USUARIOS (
  ID_USUARIO     NUMBER GENERATED BY DEFAULT AS IDENTITY
                   (MINVALUE 1 INCREMENT BY 1 START WITH 1 NOCACHE NOCYCLE)
                   NOT NULL,
  EMAIL          VARCHAR2(100 BYTE) NOT NULL,
  PASSWORD_HASH  VARCHAR2(255 BYTE) NOT NULL,
  NOMBRES        VARCHAR2(100 BYTE) NOT NULL,
  APELLIDOS      VARCHAR2(100 BYTE) NOT NULL,
  EDAD           NUMBER(3,0),
  PAIS_ORIGEN    NUMBER             NOT NULL,
  NUM_PASAPORTE  VARCHAR2(15 BYTE)  NOT NULL,
  TIPO_USUARIO   VARCHAR2(20 BYTE),
  FECHA_REGISTRO TIMESTAMP          DEFAULT CURRENT_TIMESTAMP,
  ACTIVO         NUMBER(1,0)        DEFAULT 1,
  CONSTRAINT PK_USUARIOS        PRIMARY KEY (ID_USUARIO),
  CONSTRAINT UK_USUARIOS_EMAIL  UNIQUE (EMAIL),
  CONSTRAINT UK_USUARIOS_PASAP  UNIQUE (NUM_PASAPORTE),
  CONSTRAINT CK_USUARIOS_TIPO   CHECK (TIPO_USUARIO IN ('ADMIN','REGISTRADO','ANONIMO','WEBSERVICE')),
  CONSTRAINT CK_USUARIOS_ACTIVO CHECK (ACTIVO IN (0,1))
);

-- 3. VUELOS (depende de USUARIOS via CREADO_POR)
CREATE TABLE USUARIO_AEROLINEA.VUELOS (
  ID_VUELO             NUMBER GENERATED BY DEFAULT AS IDENTITY
                         (MINVALUE 1 INCREMENT BY 1 START WITH 1 NOCACHE NOCYCLE)
                         NOT NULL,
  CODIGO_VUELO         VARCHAR2(10 BYTE)  NOT NULL,
  ORIGEN_CIUDAD        VARCHAR2(100 BYTE) NOT NULL,
  ORIGEN_CODIGO_IATA   CHAR(3 BYTE)       NOT NULL,
  DESTINO_CIUDAD       VARCHAR2(100 BYTE) NOT NULL,
  DESTINO_CODIGO_IATA  CHAR(3 BYTE)       NOT NULL,
  FECHA_SALIDA         DATE               NOT NULL,
  HORA_SALIDA          VARCHAR2(5 BYTE)   NOT NULL,
  FECHA_LLEGADA        DATE               NOT NULL,
  HORA_LLEGADA         VARCHAR2(5 BYTE)   NOT NULL,
  TIPO_ASIENTO         VARCHAR2(15 BYTE),
  PRECIO_BASE          NUMBER(10,2)       NOT NULL,
  ASIENTOS_TOTALES     NUMBER             NOT NULL,
  ASIENTOS_DISPONIBLES NUMBER             NOT NULL,
  ESTADO               VARCHAR2(15 BYTE),
  CREADO_POR           NUMBER             NOT NULL,
  FECHA_CREACION       TIMESTAMP          DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT PK_VUELOS         PRIMARY KEY (ID_VUELO),
  CONSTRAINT UK_VUELOS_CODIGO  UNIQUE (CODIGO_VUELO),
  CONSTRAINT CK_VUELOS_ASIENTO CHECK (TIPO_ASIENTO IN ('TURISTA','BUSINESS')),
  CONSTRAINT CK_VUELOS_ESTADO  CHECK (ESTADO IN ('ACTIVO','CANCELADO')),
  CONSTRAINT FK_VUELO_USUARIO  FOREIGN KEY (CREADO_POR)
    REFERENCES USUARIO_AEROLINEA.USUARIOS(ID_USUARIO)
);

-- 4. RESERVACIONES (depende de VUELOS y USUARIOS)
CREATE TABLE USUARIO_AEROLINEA.RESERVACIONES (
  ID_RESERVACION     NUMBER GENERATED BY DEFAULT AS IDENTITY
                       (MINVALUE 1 INCREMENT BY 1 START WITH 1 NOCACHE NOCYCLE)
                       NOT NULL,
  CODIGO_RESERVACION VARCHAR2(20 BYTE)  NOT NULL,
  ID_VUELO           NUMBER             NOT NULL,
  ID_USUARIO         NUMBER             NOT NULL,
  NUM_PASAJEROS      NUMBER(2,0)        NOT NULL,
  PRECIO_TOTAL       NUMBER(10,2)       NOT NULL,
  ESTADO             VARCHAR2(20 BYTE)  DEFAULT 'CONFIRMADA',
  FECHA_COMPRA       TIMESTAMP          DEFAULT CURRENT_TIMESTAMP,
  METODO_PAGO        VARCHAR2(50 BYTE),
  CONSTRAINT PK_RESERVACIONES    PRIMARY KEY (ID_RESERVACION),
  CONSTRAINT UK_RESERV_CODIGO    UNIQUE (CODIGO_RESERVACION),
  CONSTRAINT CK_RESERV_PASAJEROS CHECK (NUM_PASAJEROS > 0),
  CONSTRAINT CK_RESERV_PRECIO    CHECK (PRECIO_TOTAL > 0),
  CONSTRAINT CK_RESERV_ESTADO    CHECK (ESTADO IN ('CONFIRMADA','CANCELADA','PENDIENTE')),
  CONSTRAINT FK_RESERV_VUELO     FOREIGN KEY (ID_VUELO)
    REFERENCES USUARIO_AEROLINEA.VUELOS(ID_VUELO),
  CONSTRAINT FK_RESERV_USUARIO   FOREIGN KEY (ID_USUARIO)
    REFERENCES USUARIO_AEROLINEA.USUARIOS(ID_USUARIO)
);

-- 5. PASAJEROS (depende de RESERVACIONES)
CREATE TABLE USUARIO_AEROLINEA.PASAJEROS (
  ID_PASAJERO      NUMBER GENERATED BY DEFAULT AS IDENTITY
                     (MINVALUE 1 INCREMENT BY 1 START WITH 1 NOCACHE NOCYCLE)
                     NOT NULL,
  ID_RESERVACION   NUMBER             NOT NULL,
  NOMBRES          VARCHAR2(100 BYTE) NOT NULL,
  APELLIDOS        VARCHAR2(100 BYTE) NOT NULL,
  FECHA_NACIMIENTO DATE               NOT NULL,
  NACIONALIDAD     NUMBER             NOT NULL,
  NUM_PASAPORTE    VARCHAR2(15 BYTE)  NOT NULL,
  TIPO_ASIENTO     VARCHAR2(20 BYTE)  NOT NULL,
  CONSTRAINT PK_PASAJEROS     PRIMARY KEY (ID_PASAJERO),
  CONSTRAINT CK_PASAJ_ASIENTO CHECK (TIPO_ASIENTO IN ('TURISTA','BUSINESS')),
  CONSTRAINT FK_PASAJ_RESERV  FOREIGN KEY (ID_RESERVACION)
    REFERENCES USUARIO_AEROLINEA.RESERVACIONES(ID_RESERVACION) ON DELETE CASCADE
);

-- 6. COMENTARIOS_RATINGS (self-reference: el padre debe existir antes que el hijo)
CREATE TABLE USUARIO_AEROLINEA.COMENTARIOS_RATINGS (
  ID_COMENTARIO       NUMBER GENERATED BY DEFAULT AS IDENTITY
                        (MINVALUE 1 INCREMENT BY 1 START WITH 1 NOCACHE NOCYCLE)
                        NOT NULL,
  ID_VUELO            NUMBER         NOT NULL,
  ID_USUARIO          NUMBER         NOT NULL,
  ID_COMENTARIO_PADRE NUMBER,
  RATING              NUMBER(1,0),
  TEXTO_COMENTARIO    VARCHAR2(2000 BYTE),
  FECHA_CREACION      TIMESTAMP      DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT PK_COMENTARIOS     PRIMARY KEY (ID_COMENTARIO),
  CONSTRAINT CK_COMENTARIOS_RTG CHECK (RATING BETWEEN 1 AND 5),
  CONSTRAINT FK_COMENT_PADRE    FOREIGN KEY (ID_COMENTARIO_PADRE)
    REFERENCES USUARIO_AEROLINEA.COMENTARIOS_RATINGS(ID_COMENTARIO)
);

-- 7. HISTORIAL_BUSQUEDAS
CREATE TABLE USUARIO_AEROLINEA.HISTORIAL_BUSQUEDAS (
  ID_BUSQUEDA         NUMBER GENERATED BY DEFAULT AS IDENTITY
                        (MINVALUE 1 INCREMENT BY 1 START WITH 1 NOCACHE NOCYCLE)
                        NOT NULL,
  ID_USUARIO          NUMBER,
  ORIGEN              VARCHAR2(100 BYTE) NOT NULL,
  DESTINO             VARCHAR2(100 BYTE) NOT NULL,
  FECHA_IDA           DATE               NOT NULL,
  FECHA_VUELTA        DATE,
  NUM_PASAJEROS       NUMBER(2,0)        NOT NULL,
  TIPO_ASIENTO        VARCHAR2(20 BYTE),
  TIPO_FUENTE         VARCHAR2(10 BYTE),
  FECHA_HORA_BUSQUEDA TIMESTAMP          DEFAULT CURRENT_TIMESTAMP,
  IP_ORIGEN           VARCHAR2(45 BYTE),
  CONSTRAINT PK_HISTORIAL   PRIMARY KEY (ID_BUSQUEDA),
  CONSTRAINT CK_HIST_FUENTE CHECK (TIPO_FUENTE IN ('WEB','REST'))
);

-- 8. NOTIFICACIONES (depende de RESERVACIONES)
CREATE TABLE USUARIO_AEROLINEA.NOTIFICACIONES (
  ID_NOTIFICACION NUMBER GENERATED BY DEFAULT AS IDENTITY
                    (MINVALUE 1 INCREMENT BY 1 START WITH 1 NOCACHE NOCYCLE)
                    NOT NULL,
  ID_RESERVACION  NUMBER              NOT NULL,
  TIPO            VARCHAR2(20 BYTE),
  MENSAJE         VARCHAR2(1000 BYTE) NOT NULL,
  ENVIADO         NUMBER(1,0)         DEFAULT 0,
  FECHA_ENVIO     TIMESTAMP,
  CONSTRAINT PK_NOTIFICACIONES PRIMARY KEY (ID_NOTIFICACION),
  CONSTRAINT CK_NOTIF_TIPO     CHECK (TIPO IN ('CANCELACION','MODIFICACION','CONFIRMACION')),
  CONSTRAINT CK_NOTIF_ENVIADO  CHECK (ENVIADO IN (0,1)),
  CONSTRAINT FK_NOTIF_RESERV   FOREIGN KEY (ID_RESERVACION)
    REFERENCES USUARIO_AEROLINEA.RESERVACIONES(ID_RESERVACION)
);

-- 9. AGENCIAS_CLIENTES
CREATE TABLE USUARIO_AEROLINEA.AGENCIAS_CLIENTES (
  ID_AGENCIA            NUMBER GENERATED BY DEFAULT AS IDENTITY
                          (MINVALUE 1 INCREMENT BY 1 START WITH 1 NOCACHE NOCYCLE)
                          NOT NULL,
  NOMBRE_AGENCIA        VARCHAR2(200 BYTE) NOT NULL,
  ID_USUARIO_WEBSERVICE NUMBER             NOT NULL,
  PORCENTAJE_DESCUENTO  NUMBER(5,2)        DEFAULT 0,
  API_KEY_HASH          VARCHAR2(255 BYTE) NOT NULL,
  ACTIVO                NUMBER(1,0)        DEFAULT 1,
  FECHA_REGISTRO        TIMESTAMP          DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT PK_AGENCIAS        PRIMARY KEY (ID_AGENCIA),
  CONSTRAINT UK_AGENCIAS_USR    UNIQUE (ID_USUARIO_WEBSERVICE),
  CONSTRAINT CK_AGENCIAS_DESC   CHECK (PORCENTAJE_DESCUENTO >= 0),
  CONSTRAINT CK_AGENCIAS_ACTIVO CHECK (ACTIVO IN (0,1))
);

-- 10. HOTELES_ALIADOS (sin dependencias)
CREATE TABLE USUARIO_AEROLINEA.HOTELES_ALIADOS (
  ID_HOTEL_ALIADO      NUMBER GENERATED BY DEFAULT AS IDENTITY
                         (MINVALUE 1 INCREMENT BY 1 START WITH 1 NOCACHE NOCYCLE)
                         NOT NULL,
  NOMBRE_HOTEL         VARCHAR2(200 BYTE) NOT NULL,
  URL_API_BASE         VARCHAR2(500 BYTE) NOT NULL,
  PORCENTAJE_DESCUENTO NUMBER(5,2)        DEFAULT 0,
  ACTIVO               NUMBER(1,0)        DEFAULT 1,
  API_KEY              VARCHAR2(255 BYTE),
  FECHA_REGISTRO       TIMESTAMP          DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT PK_HOTELES        PRIMARY KEY (ID_HOTEL_ALIADO),
  CONSTRAINT CK_HOTELES_ACTIVO CHECK (ACTIVO IN (0,1))
);

-- ============================================================
-- ÍNDICES ADICIONALES
