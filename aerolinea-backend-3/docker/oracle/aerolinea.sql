  ON USUARIO_AEROLINEA.HISTORIAL_BUSQUEDAS(FECHA_HORA_BUSQUEDA);

CREATE INDEX USUARIO_AEROLINEA.IDX_NOTIFICACIONES_RESERVACION
  ON USUARIO_AEROLINEA.NOTIFICACIONES(ID_RESERVACION);

-- Índice único funcional: solo un rating por usuario+vuelo
CREATE UNIQUE INDEX USUARIO_AEROLINEA.UK_USUARIO_VUELO_RATING
  ON USUARIO_AEROLINEA.COMENTARIOS_RATINGS (
    ID_USUARIO,
    ID_VUELO,
    CASE WHEN RATING IS NOT NULL THEN 1 ELSE NULL END
  );

